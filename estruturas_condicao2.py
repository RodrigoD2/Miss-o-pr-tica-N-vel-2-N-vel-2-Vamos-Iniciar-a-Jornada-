#else if (elif) em Python

tempoexperiencia = 7

if tempoexperiencia <= 2:
    print("Nivel de conhecimento Junior.")

elif 2 < tempoexperiencia <= 5:
    print("Nivel de conhecimento pleno.")

else:
    print("Nivel de conhecimento sênior.")    